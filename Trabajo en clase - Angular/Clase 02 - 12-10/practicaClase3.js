// While
let a = 0;

console.log("-------------------\nWhile:");
while (a < 5) {
  console.log(a++);
}

// Do while
let b = 0;

console.log("-------------------\nDo while:");
do {
  console.log(b++);
} while (b < 0);

// For
console.log("-------------------\nFor:");
for (i = 0; i < 5; i++) {
  console.log(i);
}
